def sum_two_number(
        a: float, 
        b:float = 5
) -> float: 
    """Sum two values.

    Take two varabls and sum.

    Args:
        a (float): Firt number.
        b (float, optional): second number. Defults to 5.

    Returns:
        float: Sum of both arguments
    """
    num = a + b

    return num
